const month = 3 

switch (month) {
    case 1:
        console.log("JANUARY");
        break;
    case 2:
        console.log("FEBRUARY");
        break;
    case 3:
        console.log("MARCH");
        break;
    case 4:
        console.log("APRIL");
        break;
    default: 
        console.log("DEFAULT");
        break;
}
